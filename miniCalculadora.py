#Primer numero
primerN = float(input("Primer Numero:  "))
#Segundo Numero
segundoN = float(input("Segundo Numero:  "))
#sUMO LOS DOS NUMEROS
suma = primerN + segundoN
resta = primerN - segundoN
multiplicacion = primerN * segundoN
division = primerN / segundoN
modulo = primerN % segundoN
#Obtengo el resultado por pantalla
print ("La suma es: " + str(suma))
print ("La resta es: " + str(resta))
print ("La multiplicacion es: " + str(multiplicacion))
print ("La division es: " + str(division))
print ("La modulo es: " + str(modulo))